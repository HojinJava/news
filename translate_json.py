def remove_trailing_source(text: str) -> str:
    """번역문 끝에 붙어있는 출처명(AP News, BBC, 로이터, 알자지라 등)을 제거한다."""
    patterns = [
        r'\s*[-–—]\s*(AP\s*뉴스?|AP통신|로이터|BBC|알자지라|외교협회|국제위기그룹|전쟁 지역|더 워존)\s*$',
        r'\s+(AP\s*뉴스?|AP통신|로이터|BBC|알자지라|외교협회|국제위기그룹|전쟁 지역|더 워존)\s*$',
        r'\s*(x\.com|X\.com)\s*$',
    ]
    for pat in patterns:
        text = re.sub(pat, '', text, flags=re.IGNORECASE).strip()
    return text


def fix_spacing(text: str) -> str:
    """한글-영문/한글 사이 띄어쓰기 오류 수정."""
    # 이스라엘이이란 → 이스라엘이 이란
    text = re.sub(r'([가-힣])이([가-힣])', r'\1이 \2', text)
    return text


